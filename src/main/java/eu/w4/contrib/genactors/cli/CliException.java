package eu.w4.contrib.genactors.cli;

public class CliException extends Exception {

  public CliException() {
    super();
  }

  public CliException(String message, Throwable cause) {
    super(message, cause);
  }

  public CliException(String message) {
    super(message);
  }

  public CliException(Throwable cause) {
    super(cause);
  }

}
